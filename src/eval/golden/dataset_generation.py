"""Golden dataset generation infrastructure.

This module provides LLM-based generation of high-quality golden dataset examples
for evaluating the RAG system. It auto-discovers required fields from the schema
and metric registry, then uses an LLM to make judgments about chunk relevance,
expected sources, and other validation criteria.

Why Deterministic LLM (temperature=0)?
- Ensures reproducible judgments across runs
- Makes generated datasets stable and traceable
- Reduces variance in quality assessments

Why Retrieve More Chunks Than Production (k=15 vs k=5)?
- Gives LLM broader context to judge relevance
- Prevents missing relevant chunks due to retrieval ranking
- LLM selects best 3-7 from larger candidate set (quality over quantity)

Why Auto-Discovery Instead of Hardcoded Fields?
- Eliminates maintenance burden when adding metrics
- Ensures prompt always includes all required fields
- Prevents schema drift between prompt and validation
"""

import json
import logging
import re
from typing import Any

from langchain_core.language_models import BaseChatModel
from langchain_core.messages import HumanMessage, SystemMessage
from langchain_core.retrievers import BaseRetriever

from src.vectorstores.retriever import build_retriever
from src.eval.metrics.base import METRIC_REGISTRY
from src.schemas.eval import GoldenExample

logger = logging.getLogger(__name__)


# Core fields that are inputs to the generation process, not LLM judgments
_CORE_FIELDS = {"id", "question", "author", "language"}


def discover_required_fields() -> set[str]:
    """Auto-discovers required fields from GoldenExample schema and METRIC_REGISTRY.

    Discovers fields from two sources:
    1. GoldenExample Pydantic schema - fields with default_factory (list fields)
    2. METRIC_REGISTRY - required_example_fields from all registered metrics

    Excludes core input fields (id, question, author, language) as these are
    provided by the user, not generated by the LLM.

    Returns:
        Set of field names that the LLM should generate judgments for
    """
    discovered = set()

    # Discover from GoldenExample schema
    # Get all fields with default_factory (these are the optional judgment fields)
    for field_name, field_info in GoldenExample.model_fields.items():
        # Skip core fields
        if field_name in _CORE_FIELDS:
            continue

        # Include fields with default_factory (list fields like expected_chunk_ids)
        if field_info.default_factory is not None:
            discovered.add(field_name)

    # Discover from METRIC_REGISTRY
    # Collect all required_example_fields from registered metrics
    for metric_spec in METRIC_REGISTRY:
        for field in metric_spec.required_example_fields:
            # Skip core fields
            if field not in _CORE_FIELDS:
                discovered.add(field)

    return discovered


def build_field_guidance(field_name: str) -> str:
    """Returns LLM guidance for generating a specific field.

    Provides field-specific instructions with examples to help the LLM generate
    accurate values. Includes minimal guidance for currently-implemented metrics
    and a generic fallback for future fields.

    Why Separate Guidance for Each Field?
    - Makes LLM output more accurate for complex fields
    - Provides examples to guide LLM toward correct format
    - Easy to extend as new field types are added

    Args:
        field_name: Name of the field to generate guidance for

    Returns:
        Detailed guidance string for the LLM to follow
    """
    # Minimal guidance_map for currently-implemented metrics only
    guidance_map: dict[str, str] = {
        "expected_chunk_ids": """
**expected_chunk_ids**: List of chunk ID strings
- Chunk IDs that should be retrieved for this question
- Select 3-7 most relevant chunks from the candidates provided
- Use the exact chunk_id values from the candidate chunks
- Example: ["a1b2c3d4e5f6", "f6e5d4c3b2a1", "123456789abc"]
""",
        "expected_source_titles": """
**expected_source_titles**: List of source title strings
- Document titles that should appear in citations
- Extract from the 'source' field of selected chunks
- Use base title without page numbers or formatting
- Example: ["Lettres philosophiques", "Traité sur la tolérance"]
""",
    }

    # Return specific guidance if available, otherwise generic fallback
    if field_name in guidance_map:
        return guidance_map[field_name]
    else:
        # Generic fallback for fields without specific guidance
        return f"""
**{field_name}**: Generate appropriate value for this field
- Refer to the GoldenExample schema for expected type and format
- Use information from the candidate chunks to inform your judgment
- Ensure the value aligns with the question and author context
"""


def build_prompt(question: str, author: str, required_fields: set[str]) -> str:
    """Assembles standard LLM prompt with auto-discovered field requirements.

    Constructs a comprehensive prompt that instructs the LLM to:
    1. Analyze the provided candidate chunks
    2. Generate a unique ID for the example
    3. Provide judgments for all required fields
    4. Return valid JSON matching the GoldenExample schema

    Args:
        question: The philosophical question to evaluate
        author: The author who should answer (e.g., "voltaire")
        required_fields: Set of field names discovered from schema/metrics

    Returns:
        Complete prompt string ready for LLM invocation
    """
    # Build field guidance section
    field_guidance_parts = []
    for field in sorted(required_fields):  # Sort for deterministic order
        guidance = build_field_guidance(field)
        field_guidance_parts.append(guidance)

    field_guidance_section = "\n".join(field_guidance_parts)

    # Build concrete example showing exact structure
    example_fields = []
    for field in sorted(required_fields):
        if field == "expected_chunk_ids":
            example_fields.append('  "expected_chunk_ids": ["abc123def456", "xyz789uvw012", "mno345pqr678"]')
        elif field == "expected_source_titles":
            example_fields.append('  "expected_source_titles": ["Lettres philosophiques", "Traité sur la tolérance"]')
        else:
            example_fields.append(f'  "{field}": []')

    example_json = "{\n" + \
                   f'  "id": "tolerance_en",\n' + \
                   f'  "question": "{question}",\n' + \
                   f'  "author": "{author}",\n' + \
                   '  "language": "en",\n' + \
                   ",\n".join(example_fields) + "\n}"

    prompt = f"""You are creating a golden evaluation example for a RAG system where Enlightenment philosophers answer questions grounded in their historical texts.

**Task**: Generate a GoldenExample JSON object for the following question.

**Question**: {question}
**Author**: {author}

**Instructions**:
1. Review the candidate chunks provided below
2. Generate a unique ID for this example (format: "{{topic}}_{{language_code}}", e.g., "tolerance_fr")
3. Determine the language of the question (ISO 639-1 code: "fr" or "en")
4. For each required field below, make your best judgment based on the chunks:

{field_guidance_section}

**CRITICAL REQUIREMENTS**:
- Return ONLY a single JSON object
- Do NOT number your points or analyze the chunks
- Do NOT add any text before or after the JSON
- The JSON must have EXACTLY these fields: id, question, author, language, {", ".join(sorted(required_fields))}

**EXACT OUTPUT FORMAT**:
{example_json}

Now generate the actual JSON for the question above using the candidate chunks below.
"""

    return prompt


def retrieve_candidate_chunks(
    question: str,
    author: str,
    k: int = 15,
) -> list[dict[str, Any]]:
    """Fetches candidate chunks for LLM to judge relevance.

    Retrieves more chunks than production (k=15 vs k=5) to give the LLM
    broader context for making accurate judgments about which chunks are
    truly relevant.

    Args:
        question: The question to retrieve chunks for
        author: Author to filter by (for metadata filtering)
        k: Number of chunks to retrieve (default 15)

    Returns:
        List of chunk dictionaries with keys:
            - chunk_id: Unique chunk identifier
            - source: Document title
            - text: Chunk content
    """
    # Build retriever for this author
    retriever = build_retriever(author=author, k=k)

    # Retrieve candidate chunks
    docs = retriever.invoke(question)

    # Transform to simplified format for LLM prompt
    chunks = []
    for doc in docs:
        chunk = {
            "chunk_id": doc.metadata.get("chunk_id", ""),
            "source": doc.metadata.get("document_title", ""),
            "text": doc.page_content,
        }
        chunks.append(chunk)

    return chunks


def generate_golden_example_with_llm(
    question: str,
    author: str,
    language: str,
    llm: BaseChatModel,
    retriever: BaseRetriever,
) -> GoldenExample:
    """Core function returning validated GoldenExample from LLM.

    Uses an LLM with temperature=0 (deterministic) to generate golden examples
    by analyzing candidate chunks and making judgments about expected values.

    Process:
    1. Auto-discover required fields from schema and metrics
    2. Retrieve k=15 candidate chunks for LLM to analyze
    3. Build comprehensive prompt with field guidance
    4. Invoke LLM to generate JSON
    5. Parse and validate against GoldenExample schema

    Args:
        question: Philosophical question to evaluate
        author: Author who should answer (must be in AUTHOR_CONFIGS)
        language: ISO 639-1 language code ("fr" or "en")
        llm: LLM instance (should use temperature=0 for reproducibility)
        retriever: Vector store retriever for fetching candidate chunks

    Returns:
        Validated GoldenExample instance

    Raises:
        ValidationError: If LLM output doesn't match GoldenExample schema
        JSONDecodeError: If LLM output isn't valid JSON
    """
    # Step 1: Auto-discover required fields
    required_fields = discover_required_fields()

    # Step 2: Retrieve candidate chunks
    chunks = retrieve_candidate_chunks(question, author, k=15)

    # Step 3: Build prompt with field guidance
    prompt_text = build_prompt(question, author, required_fields)

    # Add candidate chunks to prompt
    chunks_section = "\n\n**Candidate Chunks**:\n"
    for i, chunk in enumerate(chunks, 1):
        chunks_section += f"\n{i}. [ID: {chunk['chunk_id']}] [Source: {chunk['source']}]\n{chunk['text']}\n"

    full_prompt = prompt_text + chunks_section

    # Step 4: Invoke LLM with system message (deterministic, temperature=0)
    system_msg = SystemMessage(content="""You are a JSON generator for a RAG evaluation system.
You MUST return ONLY valid JSON matching the exact schema provided.
Do NOT add explanations, analysis, or commentary.
Do NOT number points or describe the chunks.
Return ONLY the JSON object with the exact fields specified.""")

    human_msg = HumanMessage(content=full_prompt)

    response = llm.invoke([system_msg, human_msg])

    # Extract content from response and ensure it's a string
    # LangChain message content can be str or list (multi-modal), so convert to str
    raw_content = response.content if hasattr(response, 'content') else response
    content = str(raw_content)

    # Log the raw response for debugging
    logger.debug(f"LLM raw response:\n{content}")

    # Step 5: Parse JSON (handle markdown code blocks if present)
    # Try to extract JSON from markdown code blocks first
    json_match = re.search(r'```(?:json)?\s*\n?(.*?)\n?```', content, re.DOTALL)
    if json_match:
        json_str = json_match.group(1).strip()
    else:
        json_str = content.strip()

    # Parse JSON with better error handling
    try:
        data = json.loads(json_str)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse JSON. Raw content:\n{content}")
        logger.error(f"Extracted JSON string:\n{json_str}")
        raise ValueError(
            f"LLM returned invalid JSON: {e}\n"
            f"First 200 chars of response: {content[:200]}"
        ) from e

    # Step 6: Validate against GoldenExample schema
    example = GoldenExample(**data)

    return example
